# Fine-tuning - 1단계: 학습 데이터(JSONL) 만들기  ← 파인튜닝에서 가장 중요한 단계
# pip install openai python-dotenv
#
# ★ 핵심 개념: 파인튜닝은 '새 지식 주입'이 아니다.
#    - 최신/사내 정보를 알게 하려면 → RAG (7.rag)
#    - 파인튜닝이 잘하는 것 = '스타일·형식·톤·행동을 일관되게' 굳히기, 긴 프롬프트 줄이기.
#
# 데이터 형식: 한 줄 = 한 대화 예시. chat 형식 messages (system/user/assistant).
#   {"messages":[{"role":"system",...},{"role":"user",...},{"role":"assistant",...}]}
#   - 최소 10개, 권장 50~100+ (많을수록·일관될수록 좋음)
#   - assistant 응답이 곧 '모델이 따라 할 정답 스타일' 이다.
#
# 이 예제 목표: '항상 한 문장, 존댓말, 핵심만' 답하는 간결 고객지원 봇 톤을 학습.

import json

SYSTEM = "당신은 간결한 고객지원 봇입니다. 항상 한 문장, 존댓말로 핵심만 답합니다."

# user → assistant(따라 하길 원하는 톤) 쌍
pairs = [
    ("환불은 어떻게 하나요?", "마이페이지 > 주문내역에서 '환불 신청'을 누르시면 됩니다."),
    ("배송은 며칠 걸려요?", "보통 결제 후 2~3 영업일 내에 도착합니다."),
    ("비밀번호를 잊어버렸어요.", "로그인 화면의 '비밀번호 찾기'로 재설정하실 수 있습니다."),
    ("주문을 취소하고 싶어요.", "발송 전이라면 주문내역에서 '주문 취소'로 바로 취소됩니다."),
    ("영수증을 받을 수 있나요?", "주문내역 상세에서 '영수증 출력'을 선택하시면 됩니다."),
    ("교환도 가능한가요?", "수령 후 7일 이내 미사용 제품에 한해 교환이 가능합니다."),
    ("쿠폰은 어디서 등록하나요?", "결제 단계의 '쿠폰 적용' 칸에 코드를 입력하시면 됩니다."),
    ("고객센터 전화번호가 뭐예요?", "평일 09~18시, 1588-0000 으로 문의하실 수 있습니다."),
    ("재고가 없는데 언제 들어와요?", "재입고 알림을 신청하시면 입고 시 안내드립니다."),
    ("포인트는 어떻게 쓰나요?", "결제 시 '포인트 사용' 칸에 사용할 금액을 입력하시면 됩니다."),
    ("회원 탈퇴하려면요?", "마이페이지 > 설정 > '회원 탈퇴'에서 진행하실 수 있습니다."),
    ("해외 배송도 되나요?", "현재는 국내 배송만 지원하고 있습니다."),
]

with open("train.jsonl", "w", encoding="utf-8") as f:
    for user, assistant in pairs:
        row = {"messages": [
            {"role": "system", "content": SYSTEM},
            {"role": "user", "content": user},
            {"role": "assistant", "content": assistant},
        ]}
        f.write(json.dumps(row, ensure_ascii=False) + "\n")

print(f"train.jsonl 생성 완료 ({len(pairs)}개 예시)")
print("→ 다음: 2.create_job.py 로 업로드+학습 (assistant 톤이 일관될수록 결과가 좋습니다)")
